class MathFunct
{
	public static void main(String[] args) 
	{
		System.out.println("MAX From 20,80 : "+Math.max(20,80));
		System.out.println("MIN From 20,80 : "+Math.min(20,80));
		System.out.println("Absolute(Positive) From 20,80 : " +Math.abs(-10.2));
		System.out.println("SquareRoot of 40 : "+Math.sqrt(40));
		System.out.println("Random No. Btw 0 to 1 : "+Math.random());
		System.out.println("Sin Angle of 90 : "+Math.sin(90));
	}
}